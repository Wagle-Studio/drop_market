# script_utils.sh

logStepStart() {
    local message="$1"
    echo "_________________________________________________________________________"
    echo ""
    echo "$message"
    echo "_________________________________________________________________________"
    echo ""
}

logStepEnd() {
    echo "_________________________________________________________________________"
    echo ""
}

handleError() {
    local message="$1"
    echo "_________________________________________________________________________"
    echo ""
    echo "[Erreur] $message"
    echo "_________________________________________________________________________"
    echo ""
    exit 1
}

quitScript() {
    echo "_________________________________________________________________________"
    echo ""
    echo "Annulation. À bientôt !"
    echo "_________________________________________________________________________"
    echo ""
    exit 1
}
